package sheridan.patel.pyramid1;

public class PyramidForm {

    private String baseN="";
    private String baseSide="";
    private String height="1";

    public PyramidForm(){

    }

    public String getBaseN() {

        return baseN;
    }

    public void setBaseN(String baseN) {
        this.baseN = baseN;
    }

    public String getBaseSide() {
        return baseSide;
    }

    public void setBaseSide(String baseSide) {
        this.baseSide = baseSide;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }
}
